func (s *Service) Metadata() []ProviderDescriptor {
	if s == nil {
		return nil
	}
	providers := make([]ProviderDescriptor, 0, len(s.providers))
	order := []string{"claude", "gemini", "openai", "mistral"}
	for _, id := range order {
		provider, ok := s.providers[id]
		if !ok {
			continue
		}
		providers = append(providers, s.resolveProviderDescriptor(provider))
	}
	return providers
}

func (s *Service) resolveProviderDescriptor(provider providerDefinition) ProviderDescriptor {
	descriptor := provider.descriptor
	descriptor.Methods = append([]MethodDescriptor(nil), provider.descriptor.Methods...)
	hasConfiguredMethod := false
	for index, method := range descriptor.Methods {
		resolvedMethod := method
		if method.ID == "api_key" {
			if s.creds != nil && s.creds.Exists(descriptor.ID, method.ID) {
				resolvedMethod.Available = true
				resolvedMethod.UnavailableReason = ""
				hasConfiguredMethod = true
			} else {
				resolvedMethod.Available = false
				resolvedMethod.UnavailableReason = "Configure and save an API key in Compte IA"
			}
		}
		descriptor.Methods[index] = resolvedMethod
	}
	descriptor.Available = hasConfiguredMethod
	if !descriptor.Available {
		descriptor.UnavailableReason = "Configure and save an API key in Compte IA"
	} else {
		descriptor.UnavailableReason = ""
	}
	return descriptor
}

func (s *Service) SaveCredentials(providerID string, request SaveCredentialsRequest) (ProviderDescriptor, error) {
	if s == nil || s.creds == nil {
		return ProviderDescriptor{}, &apiError{StatusCode: http.StatusServiceUnavailable, Message: "AI workspace is unavailable"}
	}
	providerID = strings.TrimSpace(providerID)
	provider, ok := s.providers[providerID]
	if !ok {
		return ProviderDescriptor{}, &apiError{StatusCode: http.StatusBadRequest, Message: "Unsupported provider"}
	}
	methodID := strings.TrimSpace(request.Method)
	if methodID == "" {
		methodID = provider.descriptor.DefaultMethod
	}
	method, ok := provider.methods[methodID]
	if !ok {
		return ProviderDescriptor{}, &apiError{StatusCode: http.StatusBadRequest, Message: "Unsupported provider method"}
	}
	config := request.Config
	if config == nil {
		config = map[string]string{}
	}
	for _, field := range method.descriptor.Fields {
		if field.Required && strings.TrimSpace(config[field.Key]) == "" {
			return ProviderDescriptor{}, &apiError{StatusCode: http.StatusBadRequest, Message: fmt.Sprintf("Missing required provider field: %s", field.Key)}
		}
	}
	toPersist := map[string]string{}
	for _, field := range method.descriptor.Fields {
		if value := strings.TrimSpace(config[field.Key]); value != "" {
			toPersist[field.Key] = value
		}
	}
	if err := s.creds.Save(providerID, methodID, toPersist); err != nil {
		return ProviderDescriptor{}, &apiError{StatusCode: http.StatusInternalServerError, Message: fmt.Sprintf("save provider config: %v", err)}
	}
	return s.resolveProviderDescriptor(provider), nil
}

