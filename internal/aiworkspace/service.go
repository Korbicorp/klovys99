const defaultSystemPrompt = "You receive an anonymized prompt generated by klovys99. Preserve placeholder tokens exactly as they appear and answer the user's request using only the anonymized content you were given."

type Service struct {
	client    *http.Client
	providers map[string]providerDefinition
	oauth     *claudeOAuthManager
	store     *conversationStore
	creds     *credentialStore
}

type MetadataProvider interface {
	Metadata() []ProviderDescriptor
}

type CompletionProvider interface {
	Complete(ctx context.Context, request CompletionRequest) (CompletionResponse, error)
}

type ProviderDescriptor struct {
	ID                string             `json:"id"`
	Label             string             `json:"label"`
	Description       string             `json:"description"`
	Available         bool               `json:"available"`
	UnavailableReason string             `json:"unavailable_reason,omitempty"`
	DefaultMethod     string             `json:"default_method,omitempty"`
	DefaultModel      string             `json:"default_model,omitempty"`
	Methods           []MethodDescriptor `json:"methods"`
	Models            []string           `json:"models"`
}

type MethodDescriptor struct {
	ID                string        `json:"id"`
	Label             string        `json:"label"`
	Available         bool          `json:"available"`
	UnavailableReason string        `json:"unavailable_reason,omitempty"`
	Fields            []FieldSchema `json:"fields"`
}

type FieldSchema struct {
	Key         string `json:"key"`
	Label       string `json:"label"`
	InputType   string `json:"input_type"`
	Required    bool   `json:"required"`
	Secret      bool   `json:"secret,omitempty"`
	Multiline   bool   `json:"multiline,omitempty"`
	Placeholder string `json:"placeholder,omitempty"`
}

type CompletionRequest struct {
	ConversationID   string            `json:"conversation_id"`
	Provider         string            `json:"provider"`
	Method           string            `json:"method"`
	Model            string            `json:"model"`
	AnonymizedPrompt string            `json:"anonymized_prompt"`
	SystemPrompt     string            `json:"system_prompt"`
	Config           map[string]string `json:"config"`
}

type SaveCredentialsRequest struct {
	Method string            `json:"method"`
	Config map[string]string `json:"config"`
}

type CompletionResponse struct {
	ConversationID   string `json:"conversation_id"`
	Provider         string `json:"provider"`
	Method           string `json:"method"`
	Model            string `json:"model"`
	AnonymizedPrompt string `json:"anonymized_prompt"`
	ResponseText     string `json:"response_text"`
}

type providerDefinition struct {
	descriptor ProviderDescriptor
	methods    map[string]providerMethod
}

type providerMethod struct {
	descriptor MethodDescriptor
	call       providerCall
}

type providerCall func(ctx context.Context, client *http.Client, model string, systemPrompt string, messages []ConversationMessage, config map[string]string) (string, error)

type serviceContextKey struct{}

type apiError struct {
	StatusCode int
	Message    string
}

func (e *apiError) Error() string {
	return e.Message
}

func (e *apiError) GetStatusCode() int {
	return e.StatusCode
}

