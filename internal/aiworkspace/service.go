func callGemini(ctx context.Context, client *http.Client, model string, systemPrompt string, messages []ConversationMessage, config map[string]string) (providerCallResult, error) {
	endpoint := fmt.Sprintf("https://generativelanguage.googleapis.com/v1beta/models/%s:generateContent?key=%s", url.PathEscape(model), url.QueryEscape(config["api_key"]))
	contents := make([]map[string]any, 0, len(messages))
	for _, message := range messages {
		role := message.Role
		if role == "assistant" {
			role = "model"
		}
		parts := []map[string]any{{"text": messageTextWithFallback(message)}}
		if attachment := message.Attachment; attachment != nil && len(attachment.Data) > 0 && supportsNativeDocument("gemini", attachment.MediaType) {
			parts = []map[string]any{}
			if strings.TrimSpace(message.Content) != "" {
				parts = append(parts, map[string]any{"text": message.Content})
			}
			parts = append(parts, map[string]any{"inline_data": map[string]any{"mime_type": attachment.MediaType, "data": base64.StdEncoding.EncodeToString(attachment.Data)}})
		}
		contents = append(contents, map[string]any{
			"role":  role,
			"parts": parts,
		})
	}
	payload := map[string]any{
		"system_instruction": map[string]any{
			"parts": map[string]any{
				"text": systemPrompt,
			},
		},
		"contents": contents,
	}
	var response struct {
		Candidates []struct {
			Content struct {
				Parts []struct {
					Text string `json:"text"`
				} `json:"parts"`
			} `json:"content"`
		} `json:"candidates"`
	}
	if err := doJSON(ctx, client, http.MethodPost, endpoint, nil, payload, &response); err != nil {
		return providerCallResult{}, wrapProviderError("Gemini", err)
	}
	if len(response.Candidates) == 0 {
		return providerCallResult{}, &apiError{StatusCode: http.StatusBadGateway, Message: "Gemini returned no candidate"}
	}
	var builder strings.Builder
	for _, part := range response.Candidates[0].Content.Parts {
		builder.WriteString(part.Text)
	}
	return providerCallResult{ResponseText: builder.String()}, nil
}

func callOpenAI(ctx context.Context, client *http.Client, model string, systemPrompt string, messages []ConversationMessage, config map[string]string) (providerCallResult, error) {
	payloadMessages := make([]map[string]any, 0, len(messages))
	for _, message := range messages {
		content := any(messageTextWithFallback(message))
		if attachment := message.Attachment; message.Role == "user" && attachment != nil && len(attachment.Data) > 0 && supportsNativeDocument("openai", attachment.MediaType) {
			fileItem := map[string]any{"type": "input_file", "filename": attachment.Filename, "file_data": attachmentDataURL(attachment)}
			if strings.HasPrefix(strings.ToLower(attachment.MediaType), "image/") {
				fileItem = map[string]any{"type": "input_image", "image_url": attachmentDataURL(attachment)}
			}
			items := []map[string]any{fileItem}
			if strings.TrimSpace(message.Content) != "" {
				items = append(items, map[string]any{"type": "input_text", "text": message.Content})
			} else {
				items = append(items, map[string]any{"type": "input_text", "text": "Please analyze the attached file."})
			}
			content = items
		}
		payloadMessages = append(payloadMessages, map[string]any{"role": message.Role, "content": content})
	}
	payload := map[string]any{
		"model":        model,
		"instructions": systemPrompt,
		"input":        payloadMessages,
	}
	headers := map[string]string{
		"Authorization": "Bearer " + config["api_key"],
	}
	var response struct {
		Output []struct {
			Type    string `json:"type"`
			Content []struct {
				Type string `json:"type"`
				Text string `json:"text"`
			} `json:"content"`
		} `json:"output"`
	}
	if err := doJSON(ctx, client, http.MethodPost, "https://api.openai.com/v1/responses", headers, payload, &response); err != nil {
		return providerCallResult{}, wrapProviderError("OpenAI", err)
	}
	var builder strings.Builder
	for _, output := range response.Output {
		for _, content := range output.Content {
			if content.Type == "output_text" {
				builder.WriteString(content.Text)
			}
		}
	}
	if builder.Len() == 0 {
		return providerCallResult{}, &apiError{StatusCode: http.StatusBadGateway, Message: "OpenAI returned no output text"}
	}
	return providerCallResult{ResponseText: builder.String()}, nil
}

