func callClaude(ctx context.Context, client *http.Client, model string, systemPrompt string, messages []ConversationMessage, config map[string]string) (providerCallResult, error) {
	payloadMessages := make([]map[string]any, 0, len(messages))
	for _, message := range messages {
		content := any(messageTextWithFallback(message))
		if attachment := message.Attachment; attachment != nil && len(attachment.Data) > 0 && supportsNativeDocument("claude", attachment.MediaType) {
			blocks := []map[string]any{}
			if strings.TrimSpace(message.Content) != "" {
				blocks = append(blocks, map[string]any{"type": "text", "text": message.Content})
			}
			blockType := "document"
			if strings.HasPrefix(strings.ToLower(attachment.MediaType), "image/") {
				blockType = "image"
			}
			blocks = append(blocks, map[string]any{"type": blockType, "source": map[string]any{"type": "base64", "media_type": attachment.MediaType, "data": base64.StdEncoding.EncodeToString(attachment.Data)}})
			content = blocks
		}
		payloadMessages = append(payloadMessages, map[string]any{
			"role":    message.Role,
			"content": content,
		})
	}
	payload := map[string]any{
		"model":      model,
		"max_tokens": 4096,
		"system":     systemPrompt,
		"messages":   payloadMessages,
	}
	headers := map[string]string{
		"x-api-key":         config["api_key"],
		"anthropic-version": "2023-06-01",
	}
	var response struct {
		Content []struct {
			Type string `json:"type"`
			Text string `json:"text"`
		} `json:"content"`
	}
	if err := doJSON(ctx, client, http.MethodPost, "https://api.anthropic.com/v1/messages", headers, payload, &response); err != nil {
		return providerCallResult{}, wrapProviderError("Claude", err)
	}
	var builder strings.Builder
	for _, block := range response.Content {
		if block.Type == "text" {
			builder.WriteString(block.Text)
		}
	}
	return providerCallResult{ResponseText: builder.String()}, nil
}

