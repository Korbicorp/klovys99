func callClaude(ctx context.Context, client *http.Client, model string, systemPrompt string, messages []ConversationMessage, config map[string]string) (string, error) {
	payloadMessages := make([]map[string]any, 0, len(messages))
	for _, message := range messages {
		payloadMessages = append(payloadMessages, map[string]any{
			"role":    message.Role,
			"content": message.Content,
		})
	}
	payload := map[string]any{
		"model":      model,
		"max_tokens": 4096,
		"system":     systemPrompt,
		"messages":   payloadMessages,
	}
	headers := map[string]string{
		"x-api-key":         config["api_key"],
		"anthropic-version": "2023-06-01",
	}
	var response struct {
		Content []struct {
			Type string `json:"type"`
			Text string `json:"text"`
		} `json:"content"`
	}
	if err := doJSON(ctx, client, http.MethodPost, "https://api.anthropic.com/v1/messages", headers, payload, &response); err != nil {
		return "", wrapProviderError("Claude", err)
	}
	var builder strings.Builder
	for _, block := range response.Content {
		if block.Type == "text" {
			builder.WriteString(block.Text)
		}
	}
	return builder.String(), nil
}

func callClaudeOAuth(ctx context.Context, _ *http.Client, model string, systemPrompt string, messages []ConversationMessage, config map[string]string) (string, error) {
	service, _ := ctx.Value(serviceContextKey{}).(*Service)
	if service == nil || service.oauth == nil {
		return "", &apiError{StatusCode: http.StatusServiceUnavailable, Message: "Claude OAuth is unavailable"}
	}
	token, err := service.oauth.loadToken()
	if err != nil {
		return "", err
	}
	prompt := ""
	if len(messages) > 0 {
		prompt = messages[len(messages)-1].Content
	}
	responseText, err := service.oauth.runner.RunOAuthPrompt(ctx, service.oauth.configDir(), token, model, systemPrompt, prompt)
	if err != nil {
		return "", normalizeCLIError("Claude request failed", err)
	}
	return responseText, nil
}

func callGemini(ctx context.Context, client *http.Client, model string, systemPrompt string, messages []ConversationMessage, config map[string]string) (string, error) {
	endpoint := fmt.Sprintf("https://generativelanguage.googleapis.com/v1beta/models/%s:generateContent?key=%s", url.PathEscape(model), url.QueryEscape(config["api_key"]))
	contents := make([]map[string]any, 0, len(messages))
	for _, message := range messages {
		role := message.Role
		if role == "assistant" {
			role = "model"
		}
		contents = append(contents, map[string]any{
			"role": role,
			"parts": []map[string]any{
				{"text": message.Content},
			},
		})
	}
	payload := map[string]any{
		"system_instruction": map[string]any{
			"parts": map[string]any{
				"text": systemPrompt,
			},
		},
		"contents": contents,
	}
	var response struct {
		Candidates []struct {
			Content struct {
				Parts []struct {
					Text string `json:"text"`
				} `json:"parts"`
			} `json:"content"`
		} `json:"candidates"`
	}
	if err := doJSON(ctx, client, http.MethodPost, endpoint, nil, payload, &response); err != nil {
		return "", wrapProviderError("Gemini", err)
	}
	if len(response.Candidates) == 0 {
		return "", &apiError{StatusCode: http.StatusBadGateway, Message: "Gemini returned no candidate"}
	}
	var builder strings.Builder
	for _, part := range response.Candidates[0].Content.Parts {
		builder.WriteString(part.Text)
	}
	return builder.String(), nil
}

func callOpenAI(ctx context.Context, client *http.Client, model string, systemPrompt string, messages []ConversationMessage, config map[string]string) (string, error) {
	payloadMessages := []map[string]string{
		{"role": "system", "content": systemPrompt},
	}
	for _, message := range messages {
		payloadMessages = append(payloadMessages, map[string]string{
			"role":    message.Role,
			"content": message.Content,
		})
	}
	payload := map[string]any{
		"model":    model,
		"messages": payloadMessages,
	}
	headers := map[string]string{
		"Authorization": "Bearer " + config["api_key"],
	}
	var response struct {
		Choices []struct {
			Message struct {
				Content string `json:"content"`
			} `json:"message"`
		} `json:"choices"`
	}
	if err := doJSON(ctx, client, http.MethodPost, "https://api.openai.com/v1/chat/completions", headers, payload, &response); err != nil {
		return "", wrapProviderError("OpenAI", err)
	}
	if len(response.Choices) == 0 {
		return "", &apiError{StatusCode: http.StatusBadGateway, Message: "OpenAI returned no choice"}
	}
	return response.Choices[0].Message.Content, nil
}

