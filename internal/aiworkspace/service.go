func (s *Service) Metadata() []ProviderDescriptor {
	if s == nil {
		return nil
	}
	providers := make([]ProviderDescriptor, 0, len(s.providers))
	order := []string{"claude", "gemini", "openai", "mistral"}
	for _, id := range order {
		provider, ok := s.providers[id]
		if !ok {
			continue
		}
		providers = append(providers, s.resolveProviderDescriptor(provider))
	}
	return providers
}

func (s *Service) resolveProviderDescriptor(provider providerDefinition) ProviderDescriptor {
	descriptor := provider.descriptor
	descriptor.Methods = append([]MethodDescriptor(nil), provider.descriptor.Methods...)
	hasConfiguredMethod := false
	oauthAvailable, oauthUnavailableReason := false, ""
	oauthLinked := false
	if descriptor.ID == "claude" && s.oauth != nil {
		oauthAvailable, oauthUnavailableReason = s.oauth.availability()
		oauthLinked = s.oauth.status().Linked
	}
	for index, method := range descriptor.Methods {
		resolvedMethod := method
		switch method.ID {
		case "api_key":
			if s.creds != nil && s.creds.ProviderExists(descriptor.ID, method.ID) {
				resolvedMethod.Available = true
				resolvedMethod.UnavailableReason = ""
				hasConfiguredMethod = true
			} else {
				resolvedMethod.Available = false
				resolvedMethod.UnavailableReason = "Configure and save an API key in Compte IA"
			}
		case "oauth_token":
			resolvedMethod.Available = oauthLinked && oauthUnavailableReason == ""
			switch {
			case oauthUnavailableReason != "":
				resolvedMethod.UnavailableReason = oauthUnavailableReason
			case oauthLinked:
				resolvedMethod.UnavailableReason = ""
				hasConfiguredMethod = true
			case oauthAvailable:
				resolvedMethod.UnavailableReason = "Connect Claude OAuth in Compte IA"
			default:
				resolvedMethod.UnavailableReason = "Claude OAuth is unavailable"
			}
		}
		descriptor.Methods[index] = resolvedMethod
	}
	descriptor.Available = hasConfiguredMethod
	if descriptor.Available {
		descriptor.UnavailableReason = ""
		if oauthLinked && oauthUnavailableReason == "" {
			descriptor.DefaultMethod = "oauth_token"
		} else {
			descriptor.DefaultMethod = "api_key"
		}
		return descriptor
	}
	if descriptor.ID == "claude" && oauthUnavailableReason != "" {
		descriptor.UnavailableReason = oauthUnavailableReason
	} else {
		descriptor.UnavailableReason = "Configure and save an API key in Compte IA"
	}
	return descriptor
}

func (s *Service) SaveCredentials(providerID string, request SaveCredentialsRequest) (ProviderDescriptor, error) {
	if s == nil || s.creds == nil {
		return ProviderDescriptor{}, &apiError{StatusCode: http.StatusServiceUnavailable, Message: "AI workspace is unavailable"}
	}
	providerID = strings.TrimSpace(providerID)
	provider, ok := s.providers[providerID]
	if !ok {
		return ProviderDescriptor{}, &apiError{StatusCode: http.StatusBadRequest, Message: "Unsupported provider"}
	}
	methodID := strings.TrimSpace(request.Method)
	if methodID == "" {
		methodID = provider.descriptor.DefaultMethod
	}
	method, ok := provider.methods[methodID]
	if !ok {
		return ProviderDescriptor{}, &apiError{StatusCode: http.StatusBadRequest, Message: "Unsupported provider method"}
	}
	config := request.Config
	if config == nil {
		config = map[string]string{}
	}
	for _, field := range method.descriptor.Fields {
		if field.Required && strings.TrimSpace(config[field.Key]) == "" {
			return ProviderDescriptor{}, &apiError{StatusCode: http.StatusBadRequest, Message: fmt.Sprintf("Missing required provider field: %s", field.Key)}
		}
	}
	toPersist := map[string]string{}
	for _, field := range method.descriptor.Fields {
		if value := strings.TrimSpace(config[field.Key]); value != "" {
			toPersist[field.Key] = value
		}
	}
	if err := s.creds.SaveProvider(providerID, methodID, toPersist); err != nil {
		return ProviderDescriptor{}, &apiError{StatusCode: http.StatusInternalServerError, Message: fmt.Sprintf("save provider config: %v", err)}
	}
	return s.resolveProviderDescriptor(provider), nil
}

func (s *Service) ListConversations() ([]ConversationSummary, error) {
	if s == nil || s.store == nil {
		return nil, &apiError{StatusCode: http.StatusServiceUnavailable, Message: "AI workspace is unavailable"}
	}
	conversations, err := s.store.List()
	if err != nil {
		return nil, &apiError{StatusCode: http.StatusInternalServerError, Message: fmt.Sprintf("load conversations: %v", err)}
	}
	for index := range conversations {
		conversations[index].Title = s.restoreText(conversations[index].Title)
	}
	return conversations, nil
}

func (s *Service) CreateConversation() (ConversationDetail, error) {
	if s == nil || s.store == nil {
		return ConversationDetail{}, &apiError{StatusCode: http.StatusServiceUnavailable, Message: "AI workspace is unavailable"}
	}
	conversation, err := s.store.Create()
	if err != nil {
		return ConversationDetail{}, &apiError{StatusCode: http.StatusInternalServerError, Message: fmt.Sprintf("create conversation: %v", err)}
	}
	return conversation, nil
}

func (s *Service) GetConversation(id string) (ConversationDetail, error) {
	return s.getConversation(id, true)
}

func (s *Service) getConversation(id string, restore bool) (ConversationDetail, error) {
	if s == nil || s.store == nil {
		return ConversationDetail{}, &apiError{StatusCode: http.StatusServiceUnavailable, Message: "AI workspace is unavailable"}
	}
	conversation, err := s.store.Get(strings.TrimSpace(id))
	if err != nil {
		if os.IsNotExist(err) {
			return ConversationDetail{}, &apiError{StatusCode: http.StatusNotFound, Message: "Conversation not found"}
		}
		return ConversationDetail{}, &apiError{StatusCode: http.StatusInternalServerError, Message: fmt.Sprintf("load conversation: %v", err)}
	}
	if restore {
		conversation = s.restoreConversation(conversation)
	}
	return conversation, nil
}

func (s *Service) Complete(ctx context.Context, request CompletionRequest) (CompletionResponse, error) {
	if s == nil {
		return CompletionResponse{}, &apiError{StatusCode: http.StatusServiceUnavailable, Message: "AI workspace is unavailable"}
	}
	provider, ok := s.providers[strings.TrimSpace(request.Provider)]
	if !ok {
		return CompletionResponse{}, &apiError{StatusCode: http.StatusBadRequest, Message: "Unsupported provider"}
	}
	resolvedProvider := s.resolveProviderDescriptor(provider)

	methodID := strings.TrimSpace(request.Method)
	if methodID == "" {
		methodID = resolvedProvider.DefaultMethod
	}
	method, ok := provider.methods[methodID]
	resolvedMethod := method.descriptor
	if candidate := findMethodDescriptor(resolvedProvider.Methods, methodID); candidate != nil {
		resolvedMethod = *candidate
	}
	if !ok {
		return CompletionResponse{}, &apiError{StatusCode: http.StatusBadRequest, Message: "Unsupported provider method"}
	}
	if methodID == "oauth_token" && !resolvedMethod.Available {
		message := "Unsupported provider method"
		if resolvedMethod.UnavailableReason != "" {
			message = resolvedMethod.UnavailableReason
		}
		return CompletionResponse{}, &apiError{StatusCode: http.StatusBadRequest, Message: message}
	}

	prompt := strings.TrimSpace(request.AnonymizedPrompt)
	if prompt == "" && request.Attachment == nil {
		return CompletionResponse{}, &apiError{StatusCode: http.StatusBadRequest, Message: "An anonymized prompt is required"}
	}
	model := strings.TrimSpace(request.Model)
	if model == "" {
		model = provider.descriptor.DefaultModel
	}
	config := request.Config
	if config == nil {
		config = map[string]string{}
	}
	mergedConfig := map[string]string{}
	for key, value := range config {
		mergedConfig[key] = value
	}
	if s.creds != nil {
		storedConfig, err := s.creds.LoadProvider(provider.descriptor.ID, methodID)
		if err != nil {
			return CompletionResponse{}, &apiError{StatusCode: http.StatusInternalServerError, Message: fmt.Sprintf("load stored provider config: %v", err)}
		}
		for key, value := range storedConfig {
			if strings.TrimSpace(mergedConfig[key]) == "" {
				mergedConfig[key] = value
			}
		}
	}
	for _, field := range resolvedMethod.Fields {
		if field.Required && strings.TrimSpace(mergedConfig[field.Key]) == "" {
			return CompletionResponse{}, &apiError{StatusCode: http.StatusBadRequest, Message: fmt.Sprintf("Missing required provider field: %s", field.Key)}
		}
	}
	systemPrompt := strings.TrimSpace(request.SystemPrompt)
	if systemPrompt == "" {
		systemPrompt = defaultSystemPrompt
	}

	var (
		conversation ConversationDetail
		err          error
	)
	if conversationID := strings.TrimSpace(request.ConversationID); conversationID != "" {
		conversation, err = s.getConversation(conversationID, false)
		if err != nil {
			return CompletionResponse{}, err
		}
	} else {
		conversation, err = s.CreateConversation()
		if err != nil {
			return CompletionResponse{}, err
		}
	}
	attachment, warning, err := s.prepareAttachment(ctx, provider.descriptor.ID, request.Attachment)
	if err != nil {
		return CompletionResponse{}, err
	}
	if prompt == "" && (attachment == nil || attachment.Status == attachmentStatusRemoved) {
		return CompletionResponse{}, &apiError{StatusCode: http.StatusBadRequest, Message: "A prompt is required when the attachment is removed"}
	}
	userMessage := ConversationMessage{
		Role:       "user",
		Content:    prompt,
		CreatedAt:  time.Now().UTC(),
		Attachment: attachment,
	}
	transcript := append(append([]ConversationMessage{}, conversation.Messages...), userMessage)
	if err := s.loadTranscriptAttachments(transcript); err != nil {
		return CompletionResponse{}, &apiError{StatusCode: http.StatusInternalServerError, Message: fmt.Sprintf("load attachment: %v", err)}
	}
	callCtx := context.WithValue(ctx, serviceContextKey{}, s)
	callCtx = context.WithValue(callCtx, conversationContextKey{}, conversation)
	callResult, err := method.call(callCtx, s.client, model, systemPrompt, providerWindow(transcript), mergedConfig)
	if err != nil {
		return CompletionResponse{}, err
	}
	if s.creds != nil {
		secretsToPersist := map[string]string{}
		for _, field := range resolvedMethod.Fields {
			if !field.Secret {
				continue
			}
			if value := strings.TrimSpace(config[field.Key]); value != "" {
				secretsToPersist[field.Key] = value
			} else if value := strings.TrimSpace(mergedConfig[field.Key]); value != "" {
				secretsToPersist[field.Key] = value
			}
		}
		if len(secretsToPersist) > 0 {
			if err := s.creds.SaveProvider(provider.descriptor.ID, methodID, secretsToPersist); err != nil {
				return CompletionResponse{}, &apiError{StatusCode: http.StatusInternalServerError, Message: fmt.Sprintf("save provider config: %v", err)}
			}
		}
	}
	assistantMessage := ConversationMessage{
		Role:      "assistant",
		Content:   callResult.ResponseText,
		CreatedAt: time.Now().UTC(),
	}
	if strings.TrimSpace(conversation.Title) == "" {
		title := prompt
		if title == "" && attachment != nil {
			title = attachment.Filename
		}
		conversation.Title = truncateConversationTitle(title)
	}
	conversation.Provider = provider.descriptor.ID
	conversation.Method = methodID
	conversation.Model = model
	conversation.ProviderSessionID = strings.TrimSpace(callResult.ProviderSessionID)
	conversation.Messages = append(transcript, assistantMessage)
	if err := s.saveAttachment(attachment); err != nil {
		return CompletionResponse{}, &apiError{StatusCode: http.StatusInternalServerError, Message: fmt.Sprintf("save attachment: %v", err)}
	}
	for index := range conversation.Messages {
		if conversation.Messages[index].Attachment != nil {
			conversation.Messages[index].Attachment.Data = nil
		}
	}
	if conversation.CreatedAt.IsZero() {
		conversation.CreatedAt = userMessage.CreatedAt
	}
	conversation.UpdatedAt = assistantMessage.CreatedAt
	if err := s.store.Save(conversation); err != nil {
		return CompletionResponse{}, &apiError{StatusCode: http.StatusInternalServerError, Message: fmt.Sprintf("save conversation: %v", err)}
	}
	return CompletionResponse{
		ConversationID:          conversation.ID,
		Provider:                provider.descriptor.ID,
		Method:                  methodID,
		Model:                   model,
		AnonymizedPrompt:        prompt,
		ResponseText:            s.restoreText(callResult.ResponseText),
		ResponsePIIReplacements: s.buildPIIReplacements(callResult.ResponseText),
		Attachment:              attachment,
		Warning:                 warning,
	}, nil
}

func (s *Service) prepareAttachment(ctx context.Context, provider string, upload *UploadedAttachment) (*MessageAttachment, string, error) {
	if upload == nil {
		return nil, "", nil
	}
	attachment := &MessageAttachment{Filename: upload.Filename, MediaType: upload.MediaType, Size: int64(len(upload.Data))}
	if s.files == nil || !s.files.Enabled() {
		return nil, "", &apiError{StatusCode: http.StatusServiceUnavailable, Message: "File anonymization is unavailable"}
	}
	output, err := s.files.AnonymizeWithText(ctx, provider, upload.MediaType, upload.Data)
	if err == nil {
		attachment.Status = attachmentStatusAnonymized
		attachment.Reusable = true
		attachment.Data = output.Data
		attachment.Text = output.Text
		return attachment, "", nil
	}
	warning := fmt.Sprintf("File anonymization failed: %v", err)
	switch s.files.FailurePolicy() {
	case fileanonymizer.PolicyRemove:
		attachment.Status = attachmentStatusRemoved
		attachment.Warning = warning
		return attachment, warning, nil
	case fileanonymizer.PolicyPassthrough:
		attachment.Status = attachmentStatusPassthrough
		attachment.Data = upload.Data
		attachment.Warning = warning
		return attachment, warning, nil
	default:
		return nil, "", &apiError{StatusCode: http.StatusUnprocessableEntity, Message: warning}
	}
}

func (s *Service) loadTranscriptAttachments(messages []ConversationMessage) error {
	for index := range messages {
		if err := s.loadAttachmentData(messages[index].Attachment); err != nil {
			return err
		}
	}
	return nil
}

func (s *Service) restoreConversation(conversation ConversationDetail) ConversationDetail {
	conversation.Title = s.restoreText(conversation.Title)
	if len(conversation.Messages) == 0 {
		return conversation
	}
	messages := make([]ConversationMessage, len(conversation.Messages))
	for index, message := range conversation.Messages {
		messages[index] = message
		messages[index].PIIReplacements = s.buildPIIReplacements(message.Content)
		messages[index].Content = s.restoreText(message.Content)
	}
	conversation.Messages = messages
	return conversation
}

func (s *Service) restoreText(input string) string {
	if strings.TrimSpace(input) == "" || s == nil || s.anonymizer == nil {
		return input
	}
	run := s.anonymizer.NewRun()
	if run == nil {
		return input
	}
	defer func() {
		_ = run.Close()
	}()
	if restored, ok := run.Deanonymize(input); ok {
		return restored
	}
	return input
}

func (s *Service) buildPIIReplacements(input string) []PIIReplacement {
	if strings.TrimSpace(input) == "" || s == nil || s.anonymizer == nil {
		return nil
	}
	run := s.anonymizer.NewRun()
	if run == nil {
		return nil
	}
	defer func() {
		_ = run.Close()
	}()

	indices := aiWorkspaceTokenPattern.FindAllStringIndex(input, -1)
	if len(indices) == 0 {
		return nil
	}

	replacements := make([]PIIReplacement, 0, len(indices))
	outputLength := 0
	last := 0
	for _, index := range indices {
		outputLength += len(input[last:index[0]])
		token := input[index[0]:index[1]]
		value, ok := run.ValueForToken(token)
		if !ok {
			outputLength += len(token)
			last = index[1]
			continue
		}
		replacements = append(replacements, PIIReplacement{
			Type:  piiTypeFromToken(token),
			Token: token,
			Value: value,
			Start: outputLength,
			End:   outputLength + len(value),
		})
		outputLength += len(value)
		last = index[1]
	}
	if len(replacements) == 0 {
		return nil
	}
	return replacements
}

func piiTypeFromToken(token string) string {
	if len(token) < 4 || token[0] != '[' || token[len(token)-1] != ']' {
		return ""
	}
	trimmed := token[1 : len(token)-1]
	separator := strings.LastIndex(trimmed, "_")
	if separator <= 0 {
		return ""
	}
	return trimmed[:separator]
}

