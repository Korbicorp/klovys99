func doJSON(ctx context.Context, client *http.Client, method string, endpoint string, headers map[string]string, payload any, target any) error {
	body, err := json.Marshal(payload)
	if err != nil {
		return &apiError{StatusCode: http.StatusInternalServerError, Message: fmt.Sprintf("encode request: %v", err)}
	}
	request, err := http.NewRequestWithContext(ctx, method, endpoint, bytes.NewReader(body))
	if err != nil {
		return &apiError{StatusCode: http.StatusInternalServerError, Message: fmt.Sprintf("build request: %v", err)}
	}
	request.Header.Set("Content-Type", "application/json")
	for key, value := range headers {
		if strings.TrimSpace(value) != "" {
			request.Header.Set(key, value)
		}
	}
	response, err := client.Do(request)
	if err != nil {
		return &apiError{StatusCode: http.StatusBadGateway, Message: err.Error()}
	}
	defer response.Body.Close()
	responseBody, err := io.ReadAll(io.LimitReader(response.Body, 1<<20))
	if err != nil {
		return &apiError{StatusCode: http.StatusBadGateway, Message: fmt.Sprintf("read response: %v", err)}
	}
	if response.StatusCode >= http.StatusBadRequest {
		message := extractRemoteError(responseBody)
		if message == "" {
			message = strings.TrimSpace(string(responseBody))
		}
		if message == "" {
			message = response.Status
		}
		return &apiError{StatusCode: http.StatusBadGateway, Message: message}
	}
	if err := json.Unmarshal(responseBody, target); err != nil {
		return &apiError{StatusCode: http.StatusBadGateway, Message: fmt.Sprintf("decode response: %v", err)}
	}
	return nil
}

func extractRemoteError(body []byte) string {
	var payload struct {
		Error any `json:"error"`
	}
	if err := json.Unmarshal(body, &payload); err != nil {
		return ""
	}
	switch value := payload.Error.(type) {
	case string:
		return value
	case map[string]any:
		if message, ok := value["message"].(string); ok {
			return message
		}
		if message, ok := value["status"].(string); ok {
			return message
		}
	}
	return ""
}

func wrapProviderError(provider string, err error) error {
	apiErr, ok := err.(*apiError)
	if !ok {
		return err
	}
	message := strings.TrimSpace(apiErr.Message)
	if message == "" {
		message = "request failed"
	}
	return &apiError{
		StatusCode: apiErr.StatusCode,
		Message:    fmt.Sprintf("%s request failed: %s", provider, message),
	}
}
